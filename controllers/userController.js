const signOutUser = (req, res) => {
  // Here you can handle sign out logic if you are using sessions or other methods
  res.json({ msg: 'User signed out' });
};

module.exports = {
  registerUser,
  getUsers,
  getUserById,
  updateUser,
  deleteUser,
  signInUser,
  signOutUser
};
